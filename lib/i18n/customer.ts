export type CustomerLanguage = "en" | "th";

export const LANGUAGE_STORAGE_KEY = "mfl:customer-language";

export function getStoredLanguage(): CustomerLanguage {
  if (typeof window === "undefined") return "en";
  return localStorage.getItem(LANGUAGE_STORAGE_KEY) === "th" ? "th" : "en";
}

export function setStoredLanguage(language: CustomerLanguage) {
  localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
}

